    private void tbl_QL_NhanVienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_QL_NhanVienMouseClicked
        int row = tbl_QL_NhanVien.getSelectedRow();
        if (row >= 0) {
            txt_MaNV.setText(tbl_QL_NhanVien.getValueAt(row, 0).toString());
            txt_TenNV.setText(tbl_QL_NhanVien.getValueAt(row, 1).toString());
            txt_SDT.setText(tbl_QL_NhanVien.getValueAt(row, 2).toString());
            txt_DiaChi.setText(tbl_QL_NhanVien.getValueAt(row, 3).toString());
            txt_Email.setText(tbl_QL_NhanVien.getValueAt(row, 4).toString());
            String sex;
            sex = tbl_QL_NhanVien.getValueAt(tbl_QL_NhanVien.getSelectedRow(),5).toString();
            if (sex.equals("Nam")) {
                rad_nam.setSelected(true);
                rad_nu.setSelected(false);
            } else if (sex.equals("Nữ")) {
                rad_nam.setSelected(false);
                rad_nu.setSelected(true);
            }
            cb_ChucVu.setSelectedItem(tbl_QL_NhanVien.getValueAt(row, 6).toString());
            txt_Tuoi.setText(tbl_QL_NhanVien.getValueAt(row, 7).toString());
            cb_PhongBan.setSelectedItem(tbl_QL_NhanVien.getValueAt(row, 8).toString());

        }
    }//GEN-LAST:event_tbl_QL_NhanVienMouseClicked

