    private boolean validation(){
        if (txt_MaNV.getText().isEmpty() && !txt_MaNV.getText().matches("^NV\\d+$")) {
            JOptionPane.showMessageDialog(null, "Vui lòng nhập mã nhân viên.", "Lỗi", JOptionPane.INFORMATION_MESSAGE);
            return false;
        }
        
        if (txt_TenNV.getText().isEmpty() && !txt_TenNV.getText().matches("([A-Z]{1}[a-z])+")) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập tên nhân viên.", "Lỗi", JOptionPane.INFORMATION_MESSAGE);
            return false;
        }
        
        if (txt_SDT.getText().isEmpty() && !txt_SDT.getText().matches("[0][0-9]{10}")) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập số điện thoại nhân viên.", "Lỗi", JOptionPane.INFORMATION_MESSAGE);
            return false;
        }
        
        if (txt_DiaChi.getText().isEmpty() && !txt_DiaChi.getText().matches("[0-9a-zA-Z\\s]+")) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập địa chỉ nhân viên.", "Lỗi", JOptionPane.INFORMATION_MESSAGE);
            return false;
        }
        if (txt_Tuoi.getText().isEmpty() && Integer.parseInt(txt_Tuoi.getText())<0) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập tuổi nhân viên.", "Lỗi", JOptionPane.INFORMATION_MESSAGE);
            return false;
        }
        if (txt_Email.getText().isEmpty() && !txt_Email.getText().matches("[A-Za-z0-9]+@[gmail.com]")) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập email nhân viên.", "Lỗi", JOptionPane.INFORMATION_MESSAGE);
            return false;
        }
        
        return true;
    }
    private void LayNguonSP() {
        tbl_QL_NhanVien.removeAll();
        try {
            ConnectDB.getInstance().connect();
        } catch (SQLException e) {
                // TODO Auto-generated catch block
            e.printStackTrace();
        }
        
        nv_DAO = new NhanVien_DAO();
        List<NhanVien> listNV = nv_DAO.getAllTbNhanVien();
        if (tbl_QL_NhanVien.getModel() == null) {
            String[] arr = {"Mã NV", "Ten NV", "SDT", "Địa Chỉ", "Email", "Giới Tính", "Chức vụ", "Tuổi", "Mã Phòng Ban"};
            DefaultTableModel model = new DefaultTableModel(arr, 0);
            tbl_QL_NhanVien.setModel(model);
        }
        DefaultTableModel model = (DefaultTableModel) tbl_QL_NhanVien.getModel();
        model.setRowCount(0);
        
        for(NhanVien nv: listNV) {
            model.addRow(new Object[] {nv.getMaNhanVien(),
                nv.getHoTen(), nv.getSdt(), nv.getDiaChi(),
                nv.getEmail(), nv.getGioiTinh()?"Nam":"Nu", nv.getChucVu(), 
                nv.getTuoi(),nv.getMaPhongBan().getMaPhongBan()});
        }
    }
