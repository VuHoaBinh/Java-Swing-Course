    private void btnLuuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnLuuMouseClicked
        // TODO add your handling code here:
        if(isFlag.equalsIgnoreCase("update")){
            try 
            {
                ConnectDB.getInstance().connect();
            } catch (SQLException e) {
                    // TODO Auto-generated catch block
                e.printStackTrace();
            }
            nv_DAO = new NhanVien_DAO();
            int rowChoice = tbl_QL_NhanVien.getSelectedRow(); 
            
            
            String maNhanVien = txt_MaNV.getText();
            
            
            if(validation() == true){
            
                String hoTen = txt_TenNV.getText();
                String sdt = txt_SDT.getText();

                String diaChi = txt_DiaChi.getText();
                String email = txt_Email.getText();
                boolean phai = rad_nam.isSelected();
                String chucVu = cb_ChucVu.getSelectedItem().toString();
                int tuoi = Integer.parseInt(txt_Tuoi.getText());
                String phongBan = cb_PhongBan.getSelectedItem().toString();

                PhongBan phban = new PhongBan(phongBan);
                NhanVien nv = new NhanVien(maNhanVien, hoTen, sdt,diaChi, email, phai,chucVu,tuoi, phban);
                try {
                    nv_DAO.update(nv);
                    if (tbl_QL_NhanVien.getModel() == null) {
                        String[] arr = {"Mã NV", "Ten NV", "SDT", "Địa Chỉ", "Email", "Giới Tính", "Chức vụ", "Tuổi", "Mã Phòng Ban"};
                        DefaultTableModel model = new DefaultTableModel(arr, 0);
                        tbl_QL_NhanVien.setModel(model);
                    }
                    DefaultTableModel model = (DefaultTableModel) tbl_QL_NhanVien.getModel();

                    model.setValueAt(maNhanVien, rowChoice, 0);
                    model.setValueAt(hoTen, rowChoice, 1);
                    model.setValueAt(sdt, rowChoice, 2);
                    model.setValueAt(diaChi, rowChoice, 3);
                    model.setValueAt(email, rowChoice, 4);
                    model.setValueAt(phai?"Nam":"Nu", rowChoice, 5);
                    model.setValueAt(chucVu, rowChoice, 6);
                    model.setValueAt(tuoi, rowChoice, 7);
                    model.setValueAt(phongBan, rowChoice, 8);

                } catch (Exception e1) {
                    JOptionPane.showMessageDialog(this, "Fail");
                }
                isFlag = "";
                KhoaMo(false);
            }
        }
        else{
            try {
                ConnectDB.getInstance().connect();
            } catch (SQLException e) {
                e.printStackTrace();
            }
            nv_DAO = new NhanVien_DAO();
            if(validation()==true){
            
                String maNhanVien = txt_MaNV.getText();
                String hoTen = txt_TenNV.getText();
                String sdt = txt_SDT.getText();
                String diaChi = txt_DiaChi.getText();
                String email = txt_Email.getText();
                boolean phai = rad_nam.isSelected();
                int tuoi = Integer.parseInt(txt_Tuoi.getText());
                String chucVu = cb_ChucVu.getSelectedItem().toString();
                String phongBan = cb_PhongBan.getSelectedItem().toString();

                PhongBan phban = new PhongBan(phongBan);
                NhanVien nv = new NhanVien(maNhanVien, hoTen, sdt,diaChi, email, phai, chucVu , tuoi, phban);



                try {
                    nv_DAO.create(nv);
                    if (tbl_QL_NhanVien.getModel() == null) {
                        String[] arr = {"Mã NV", "Ten NV", "SDT", "Địa Chỉ", "Email", "Giới Tính", "Chức vụ", "Tuổi", "Mã Phòng Ban"};
                        DefaultTableModel model = new DefaultTableModel(arr, 0);
                        tbl_QL_NhanVien.setModel(model);
                    }
                    DefaultTableModel model = (DefaultTableModel) tbl_QL_NhanVien.getModel();

                    model.addRow(new Object[] { nv.getMaNhanVien(),
                                        nv.getHoTen(), nv.getSdt(), nv.getDiaChi(),
                                        nv.getEmail(), nv.getGioiTinh()?"Nam":"Nu" , nv.getChucVu() ,nv.getTuoi(), nv.getMaPhongBan().getMaPhongBan()});
                } catch (Exception e1) {
                    e1.printStackTrace();
                    JOptionPane.showMessageDialog(this, "Trùng");
                }
                isFlag = "";
                KhoaMo(false);
            }
        }
    }//GEN-LAST:event_btnLuuMouseClicked


