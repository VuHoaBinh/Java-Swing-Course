    private void btn_timkiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_timkiemActionPerformed
        if (txt_timkiem.getText().equals("")) {
            LayNguonSP();
        }
        try {
            ConnectDB.getInstance().connect();
            String timkiem = txt_timkiem.getText();

            // Xóa toàn bộ dữ liệu trong tbl_QL_NhanVien
            DefaultTableModel model = (DefaultTableModel) tbl_QL_NhanVien.getModel();
            model.setRowCount(0);

            nv_DAO = new NhanVien_DAO();
            ArrayList<NhanVien> listNV = nv_DAO.getNVTheoMa(timkiem);

            if (model.getColumnCount() == 0) {
                String[] arr = {"Mã NV", "Ten NV", "SDT", "Địa Chỉ", "Email", "Giới Tính", "Chức vụ", "Tuổi", "Mã Phòng Ban"};
                model.setColumnIdentifiers(arr);
            }
            for(NhanVien nv: listNV) {
                model.addRow(new Object[] {nv.getMaNhanVien(),
                    nv.getHoTen(), nv.getSdt(), nv.getDiaChi(),
                    nv.getEmail(), nv.getGioiTinh()?"Nam":"Nu", nv.getChucVu(), 
                    nv.getTuoi(),nv.getMaPhongBan().getMaPhongBan()});
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btn_timkiemActionPerformed

