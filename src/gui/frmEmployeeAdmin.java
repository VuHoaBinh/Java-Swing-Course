    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        try {
            ConnectDB.getInstance().connect();
        } catch (SQLException e) {
                // TODO Auto-generated catch block
            e.printStackTrace();
        }
        boolean b= true;
        txt_MaNV.setEditable(b);
        txt_TenNV.setEditable(b);
        txt_Email.setEditable(b);
        txt_SDT.setEditable(b);
        txt_DiaChi.setEnabled(b);
        cb_ChucVu.setEnabled(b);
        txt_Tuoi.setEditable(b);
        cb_PhongBan.setEditable(b);
        
        
        btnThem.setEnabled(b);
        btnSua.setEnabled(b);
        btnXoa.setEnabled(b);

        btnLuu.setEnabled(!b);
        btnHuy.setEnabled(!b);
        
        nv_DAO = new NhanVien_DAO();
        if (tbl_QL_NhanVien.getSelectedRow() >= 0) {
            int row = tbl_QL_NhanVien.getSelectedRow();
            String ma = txt_MaNV.getText();
            NhanVien nv = new NhanVien(ma);
            try {
                nv_DAO.delete(nv);
                DefaultTableModel model = (DefaultTableModel) tbl_QL_NhanVien.getModel();
                model.removeRow(row);
            } catch (Exception e1) {
                JOptionPane.showMessageDialog(this, "Not delete");
            }
        }else{
            JOptionPane.showMessageDialog(this, "Vui lòng chọn dòng để xóa.");
        }
        xoatrang();
        
    }//GEN-LAST:event_btnXoaActionPerformed

