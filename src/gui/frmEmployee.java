    private void btn_timkiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_timkiemActionPerformed
        if (txt_timkiem.getText().equals("")) {
            LayNguonSP();
        }
        try {
            timkiem = txt_timkiem.getText();
            tbl_QL_NhanVien.removeAll();
            String[] arr = {"Mã NV", "Tên NV", "Email", "SDT", "Chức Vụ", "Giới Tính", "Địa Chỉ", "Hình"};
            DefaultTableModel model = new DefaultTableModel(arr, 0); //tạo TableModel chứa dữ liệu cho Jtable
            String sql = "select * from TT_NhanVien where TenNV like N'%" + timkiem + "%'";

            Connection connection = ConnectDB.getConnection();
            PreparedStatement ps = connection.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();

            // hiển thị dữ liệu lên jtable
            while (rs.next()) {
                Vector vector = new Vector();
                vector.add(rs.getString("MaNV"));
                vector.add(rs.getString("TenNV"));
                vector.add(rs.getString("Email"));
                vector.add(rs.getString("SDT"));
                vector.add(rs.getString("ChucVu"));
                vector.add(rs.getString("GioiTinh"));
                vector.add(rs.getString("DiaChi"));
                vector.add(rs.getString("Hinh"));

                model.addRow(vector);
            }
            tbl_QL_NhanVien.setModel(model);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btn_timkiemActionPerformed

