    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        StringBuilder sb = new StringBuilder();
        if (txt_MaNV.getText().equals("") || txt_TenNV.getText().equals("") || txt_Email.getText().equals("") || txt_SDT.getText().equals("") || txt_DiaChi.getText().equals("")) {
            sb.append("không Được Để Nội Dung Các ô Trống");
        }
        if (sb.length() > 0) {
            JOptionPane.showMessageDialog(this, sb.toString());
            return;
        }

        if (ktThem == true) {
            try {
                if (ktTrungThem() == true) {
                    JOptionPane.showMessageDialog(null, "Trùng mã NV: " + txt_MaNV.getText() + " !"/*, "Error", JOptionPane.ERROR_MESSAGE*/);
                    return;
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
        try {
            Connection conn = ConnectDB.getConnection();
            if (ktThem == true) {  // --------------insert
                String sql = "INSERT INTO TT_NhanVien (MaNV, TenNV, Email, SDT, ChucVu, GioiTinh, DiaChi, Hinh) values(?,?,?,?,?,?,?,?)";
                try {
                    Connection con = ConnectDB.getConnection();
                    PreparedStatement pstmt = con.prepareStatement(sql);

                    pstmt.setString(1, txt_MaNV.getText());
                    pstmt.setString(2, txt_TenNV.getText());
                    pstmt.setString(3, txt_Email.getText());
                    pstmt.setString(4, txt_SDT.getText());
                    pstmt.setString(5, cb_ChucVu.getSelectedItem().toString());
                    String a = "";
                    if (rdb_nam.isSelected()) {
                        a = "Nam";
                    } else if (rdb_nu.isSelected()) {
                        a = "Nữ";
                    }
                    pstmt.setString(6, a);
                    pstmt.setString(7, txt_DiaChi.getText());
                    Blob hinh = new SerialBlob(anh);
                    pstmt.setBlob(8, hinh);
                    pstmt.executeUpdate();
                    ConnectDB.closeConnection(conn);
                } catch (Exception e) {
                }
            } else {
                String sql = "update TT_NhanVien set TenNV=?,Email=?,SDT=?,ChucVu=?,GioiTinh=?,DiaChi=?,Hinh=? where MaNV=?";
                try {
                    Connection con = ConnectDB.getConnection();
                    PreparedStatement pstmt = con.prepareStatement(sql);

                    pstmt.setString(8, txt_MaNV.getText());
                    pstmt.setString(1, txt_TenNV.getText());
                    pstmt.setString(2, txt_Email.getText());
                    pstmt.setString(3, txt_SDT.getText());
                    pstmt.setString(4, cb_ChucVu.getSelectedItem().toString());
                    if (rdb_nam.isSelected()) {
                        pstmt.setString(5, "Nam");
                    } else {
                        pstmt.setString(5, "Nữ");
                    }
                    pstmt.setString(6, txt_DiaChi.getText());
                    Blob hinh = new SerialBlob(anh);
                    pstmt.setBlob(7, hinh);

                    pstmt.executeUpdate();
                    ConnectDB.closeConnection(conn);
                } catch (Exception e) {
                }
            }

            if (ktThem == true) {
                JOptionPane.showMessageDialog(null, "Thêm thành công");
            } else {
                JOptionPane.showMessageDialog(null, "Sửa thành công");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        KhoaMo(false);
        LayNguonSP();
        txt_MaNV.setEditable(false);
    }//GEN-LAST:event_btnLuuActionPerformed

