     private boolean validation(){
        if (txtMaSP.getText().isEmpty() && !txtMaSP.getText().matches("^SP\\d+$")) {
            JOptionPane.showMessageDialog(null, "Vui lòng nhập mã sản phẩm.", "Lỗi", JOptionPane.INFORMATION_MESSAGE);
            return false;
        }
        
        if (txtTenSp.getText().isEmpty() && !txtTenSp.getText().matches("([A-Z]{1}[a-z])+")) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập tên sản phẩm.", "Lỗi", JOptionPane.INFORMATION_MESSAGE);
            return false;
        }
        
        if (txtNamSX.getText().isEmpty() && Integer.parseInt(txtNamSX.getText()) < 1990) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập năm sản xuất.", "Lỗi", JOptionPane.INFORMATION_MESSAGE);
            return false;
        }
        if (txtGiaBan.getText().isEmpty() && Integer.parseInt(txtGiaBan.getText()) < 0) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập giá.", "Lỗi", JOptionPane.INFORMATION_MESSAGE);
            return false;
        }
        if (txtSoLuong.getText().isEmpty() && Integer.parseInt(txtSoLuong.getText())<0) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập số lượng.", "Lỗi", JOptionPane.INFORMATION_MESSAGE);
            return false;
        }
        
        return true;
    }
    private void LayNguonSP() {
        tblSanPham.removeAll();
        try {
            ConnectDB.getInstance().connect();
        } catch (Exception e) {
                // TODO Auto-generated catch block
            e.printStackTrace();
        }
        sp_DAO = new SanPham_DAO();
        List<SanPham> listSP  = sp_DAO.getAllTableSanPham();
        
        if (tblSanPham.getModel() == null) {
            String[] arr = {"Mã SP", "Tên SP", "Loại Xe", "Nhà CC", "Giá Bán", "Năm SX", "Số Lượng" ,"Xi lanh", "Màu Sắc"};
            DefaultTableModel model = new DefaultTableModel(arr, 0);
            tblSanPham.setModel(model);
        }
        DefaultTableModel model = (DefaultTableModel) tblSanPham.getModel();
        model.setRowCount(0);
        for(SanPham sp: listSP) {
            model.addRow(new Object[] {sp.getMaSanPham(),
                sp.getTenXe(), sp.getMaLoai().getMaLoai(),
                sp.getNhaPhanPhoi().getMaNhaPhanPhoi(), sp.getGia(), sp.getNamSanXuat() , sp.getSoLuong(),
                sp.getXiLanh(),sp.getMauSac()});
        }
    }
    
