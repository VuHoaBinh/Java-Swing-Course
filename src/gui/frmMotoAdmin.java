    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        if(isFlag.equals("them")){
            try {
                ConnectDB.getInstance().connect();
            } catch (Exception e) {
                e.printStackTrace();
            }
            sp_DAO = new SanPham_DAO();
            if(validation()==true){
                String MaSP = txtMaSP.getText();
                String TenSp = txtTenSp.getText();
                String Loaixe = cb_LoaiXe.getSelectedItem().toString();
                String NhaCC = cbNhaCC.getSelectedItem().toString();
                double GiaBan = Double.parseDouble(txtGiaBan.getText());
                int NamSX = Integer.parseInt(txtNamSX.getText());
                int SoLuong = Integer.parseInt(txtSoLuong.getText());
                String Xilanh = cbXiLanh.getSelectedItem().toString();
                String Mausac = cbMauSac.getSelectedItem().toString();


                LoaiXe LoaiXe = new LoaiXe(Loaixe);
                NhaPhanPhoi NPP = new NhaPhanPhoi(NhaCC);

                SanPham sp = new SanPham(MaSP, TenSp, LoaiXe, NPP , GiaBan, NamSX, SoLuong, Xilanh, Mausac);
                try {
                    sp_DAO.create(sp);
                    tblSanPham.removeAll();
                    if (tblSanPham.getModel() == null) {
                        String[] arr = {"Mã SP", "Tên SP", "Loại Xe", "Nhà CC", "Giá Bán", "Năm SX", "Số Lượng" ,"Xi lanh", "Màu Sắc"};
                        DefaultTableModel model = new DefaultTableModel(arr, 0);
                        tblSanPham.setModel(model);
                    }
                    DefaultTableModel model = (DefaultTableModel) tblSanPham.getModel();
                    model.addRow(new Object[] {sp.getMaSanPham(),
                                    sp.getTenXe(), sp.getMaLoai().getMaLoai(),
                                    sp.getNhaPhanPhoi().getMaNhaPhanPhoi(), sp.getNamSanXuat() , sp.getGia(), sp.getSoLuong(),
                                    sp.getXiLanh(),sp.getMauSac()}
                    );
                } catch (Exception e1) {
                        e1.printStackTrace();
                        JOptionPane.showMessageDialog(this, "Trùng");
                }

                isFlag = "";
                KhoaMo(false);
            }
        }else{
            try {
                ConnectDB.getInstance().connect();
            } catch (Exception e) {
                    // TODO Auto-generated catch block
                e.printStackTrace();
            }
            sp_DAO = new SanPham_DAO();
            int rowChoice = tblSanPham.getSelectedRow(); 
            
            String maSanPham = txtMaSP.getText();
            
            if(validation() == true){
            
                String MaSP = txtMaSP.getText();
                String TenSp = txtTenSp.getText();
                String Loaixe = cb_LoaiXe.getSelectedItem().toString();
                String NhaCC = cbNhaCC.getSelectedItem().toString();
                double GiaBan = Double.parseDouble(txtGiaBan.getText());
                int NamSX = Integer.parseInt(txtNamSX.getText());
                int SoLuong = Integer.parseInt(txtSoLuong.getText());
                String Xilanh = cbXiLanh.getSelectedItem().toString();
                String Mausac = cbMauSac.getSelectedItem().toString();


                LoaiXe LoaiXe = new LoaiXe(Loaixe);
                NhaPhanPhoi NPP = new NhaPhanPhoi(NhaCC);

                SanPham sp = new SanPham(MaSP, TenSp, LoaiXe, NPP , GiaBan, NamSX, SoLuong, Xilanh, Mausac);
                try {
                    sp_DAO.update(sp);
                    if (tblSanPham.getModel() == null) {
                        String[] arr = {"Mã SP", "Tên SP", "Loại Xe", "Nhà CC", "Giá Bán", "Năm SX", "Số Lượng" ,"Xi lanh", "Màu Sắc"};
                        DefaultTableModel model = new DefaultTableModel(arr, 0);
                        tblSanPham.setModel(model);
                    }
                    DefaultTableModel model = (DefaultTableModel) tblSanPham.getModel();

                    model.setValueAt(MaSP, rowChoice, 0);
                    model.setValueAt(TenSp, rowChoice, 1);
                    model.setValueAt(Loaixe, rowChoice, 2);
                    model.setValueAt(NhaCC, rowChoice, 3);
                    model.setValueAt(GiaBan, rowChoice, 4);
                    model.setValueAt(NamSX, rowChoice, 5);
                    model.setValueAt(SoLuong, rowChoice, 6);
                    model.setValueAt(Xilanh, rowChoice, 7);
                    model.setValueAt(Mausac, rowChoice, 8);

                } catch (Exception e1) {
                    JOptionPane.showMessageDialog(this, "Fail");
                }
                isFlag = "";
                KhoaMo(false);
            }
        }
            
        
    }//GEN-LAST:event_btnLuuActionPerformed

