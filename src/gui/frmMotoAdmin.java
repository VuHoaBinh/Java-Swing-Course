    private void btn_timkiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_timkiemActionPerformed
        if (txt_timkiem.getText().equals("")) {
            LayNguonSP();
        }
        try {
            ConnectDB.getInstance().connect();
            String timkiem = txt_timkiem.getText();
            DefaultTableModel model = (DefaultTableModel) tblSanPham.getModel();
            model.setRowCount(0);
            sp_DAO = new SanPham_DAO();
            ArrayList<SanPham> listSP = sp_DAO.getSanPhamTheoMa(timkiem);
            if (model.getColumnCount() == 0) {
               String[] arr = {"Mã SP", "Tên SP", "Loại Xe", "Nhà CC", "Giá Bán", "Năm SX", "Số Lượng" ,"Xi lanh", "Màu Sắc"};
                model.setColumnIdentifiers(arr);
            }
            for(SanPham sp: listSP) {
                model.addRow(new Object[] { sp.getMaSanPham(),
                                sp.getTenXe(), sp.getMaLoai().getMaLoai(),
                                sp.getNhaPhanPhoi().getMaNhaPhanPhoi(), sp.getNamSanXuat() , sp.getGia(), sp.getSoLuong(),
                                sp.getXiLanh(),sp.getMauSac()});
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        
        
    }//GEN-LAST:event_btn_timkiemActionPerformed

