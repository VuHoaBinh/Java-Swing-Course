    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        try {
            ConnectDB.getInstance().connect();
        } catch (Exception e) {
                // TODO Auto-generated catch block
            e.printStackTrace();
        }
        
        boolean b= true;
        txtMaSP.setEditable(b);
        txtTenSp.setEditable(b);
        txtNamSX.setEditable(b);
        txtGiaBan.setEditable(b);
        txtSoLuong.setEditable(b);
        cb_LoaiXe.setEnabled(b);
        cbNhaCC.setEnabled(b);
        cbMauSac.setEnabled(b);
        cbXiLanh.setEnabled(b);

        btnThem.setEnabled(b);
        btnSua.setEnabled(b);
        btnXoa.setEnabled(b);

        btnLuu.setEnabled(!b);
        btnHuy.setEnabled(!b);
        
        
        sp_DAO = new SanPham_DAO();
        
        if (tblSanPham.getSelectedRow() >= 0) {
            int row = tblSanPham.getSelectedRow();
            String ma = txtMaSP.getText();
            SanPham sp = new SanPham(ma);
            try {
                sp_DAO.delete(sp);
                DefaultTableModel model = (DefaultTableModel) tblSanPham.getModel();
                model.removeRow(row);
            } catch (Exception e1) {
                    JOptionPane.showMessageDialog(this, "Not delete");
            }
        }else{
            JOptionPane.showMessageDialog(this, "Vui lòng chọn dòng để xóa.");
        }
        xoatrang();
    }//GEN-LAST:event_btnXoaActionPerformed

